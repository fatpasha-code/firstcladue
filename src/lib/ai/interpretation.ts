import Anthropic from '@anthropic-ai/sdk'
import { InterpretationSchema, type Interpretation, type ExtractedData } from './schemas'

const client = new Anthropic()

export async function runInterpretation(rawText: string, extractedData: ExtractedData): Promise<Interpretation> {
  const model = process.env.CLAUDE_INTERPRETATION_MODEL
  if (!model) {
    throw new Error('CLAUDE_INTERPRETATION_MODEL env var is not set')
  }

  try {
    const message = await client.messages.stream({
      model,
      max_tokens: 4096,
      system: `Ты — управленческий аналитик. Твоя задача — интерпретировать технический разговор на языке менеджера.

Весь вывод строго на русском языке.

ГЛАВНОЕ ПРАВИЛО: пиши ТОЛЬКО то, что явно следует из исходного текста. Не додумывай детали, не добавляй проценты готовности, не используй термины которых не было в разговоре. Если что-то не было сказано — это не факт.

Что значит "явно следует":
- Если в разговоре назвали конкретный критерий успеха — зафиксируй его дословно
- Если была чёткая зависимость между участниками — укажи её
- Если команда сама назвала компромисс — отрази его

Что запрещено:
- Псевдоточность: "готово на 80%", "риск 70%" — если этого не говорили
- Добавлять термины из другого контекста ("спецификации", "автоматические тесты", "бэкенд для фронтенда")
- Использовать формальные слова там где в разговоре звучали простые: если говорили "сообщение" или "короткое описание" — не заменять на "спецификацию" или "документацию"
- Додумывать проблемы которых не было в разговоре
- Уровень детализации который не соответствует зрелости разговора

Формат полей:
- summary: 2-3 предложения. Что сделано, что блокирует, на каких условиях двигаемся дальше. Только факты из текста.
- management_view: что менеджеру нужно знать и сделать. На что обратить внимание прямо сейчас. Без воды.
- hidden_blockers: зависимости и риски которые явно есть в тексте, но команда их не назвала блокерами явно. Максимум 2-3. Только если реально есть в тексте. Приоритизируй риски которые ломают сквозной пользовательский сценарий — они важнее технических рисков отдельных компонентов.
- ambiguities: вещи которые остались неясными после разговора. Фокус на операционных неясностях: что именно считается выполненным, что считается "стабильным", какое минимальное условие достаточно. Только реальные неясности из текста.
- clarification_questions: вопросы которые менеджер должен задать. Соответствуют уровню зрелости и контексту разговора. Максимум 3.
- real_status: green (всё идёт по плану) | yellow (есть риски, но движемся) | red (есть блокеры которые останавливают прогресс)

Верни ТОЛЬКО валидный JSON без markdown, без \`\`\`json, без пояснений:
{
  "summary": "...",
  "management_view": "...",
  "hidden_blockers": ["..."],
  "ambiguities": ["..."],
  "clarification_questions": ["..."],
  "real_status": "green|yellow|red"
}`,
      messages: [{ role: 'user', content: 'Исходный текст:\n\n' + rawText + '\n\nИзвлечённые данные:\n\n' + JSON.stringify(extractedData, null, 2) }],
    }).finalMessage()

    const text = message.content[0].type === 'text' ? message.content[0].text : ''
    const json = JSON.parse(text.trim())
    return InterpretationSchema.parse(json)
  } catch (error) {
    console.error('[runInterpretation] error:', error)
    throw error
  }
}
